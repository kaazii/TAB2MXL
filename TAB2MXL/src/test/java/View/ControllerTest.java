package View;

import static org.junit.jupiter.api.Assertions.*;

import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;

import View.Controller.Type;

class ControllerTest {
	public Controller controller;
	@BeforeEach
	void setUp() throws Exception {
		controller = new Controller();
	}

	@Test
	void testCleanUp() {
		
		String result = Controller.cleanup("Andantino\n"
				+ " \n"
				+ "    arm.\n"
				+ "     |       |       |          |          |        |\n"
				+ "E|---------------------------|-15p12-10p9-12p10-6p5-8p6-----|\n"
				+ "B|---------------------------|--------------------------8-5-|\n"
				+ "G|---------------------------|------------------------------|\n"
				+ "D|--[7]----------------------|------------------------------|\n"
				+ "A|--[7]----------------------|------------------------------|\n"
				+ "D|--[7]----------------------|------------------------------|\n"
				+ "                                4  1  2 1  4  2 2 1 4 2 4 1\n"
				+ "");
		assertEquals("E|---------------------------|-15p12-10p9-12p10-6p5-8p6-----|\n"
				+ "B|---------------------------|--------------------------8-5-|\n"
				+ "G|---------------------------|------------------------------|\n"
				+ "D|--[7]----------------------|------------------------------|\n"
				+ "A|--[7]----------------------|------------------------------|\n"
				+ "D|--[7]----------------------|------------------------------|\n\n",result);
		
	}
	
	void testRepeatCleanup() {
		String result = Controller.repeatCleanUp(" |----REPEAT-2X---|                   \n"
				+ "CC|x---------------|--------x-------|\n"
				+ "HH|--x-x-x-x-x-x-x-|----------------|\n"
				+ "SD|----o-------o---|oooo------------|\n"
				+ "HT|----------------|----oo----------|\n"
				+ "MT|----------------|------oo--------|\n"
				+ "BD|o-------o-------|o-------o-------|\n"
				+ "");
		assertEquals("CC|x---------------|--------x-------|\n"
				+ "HH|--x-x-x-x-x-x-x-|----------------|\n"
				+ "SD|----o-------o---|oooo------------|\n"
				+ "HT|----------------|----oo----------|\n"
				+ "MT|----------------|------oo--------|\n"
				+ "BD|o-------o-------|o-------o-------|\n",result);
	}

}
